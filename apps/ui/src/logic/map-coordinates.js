/**
 * Parse SVG path data and extract coordinate points
 * @param {string} pathData - SVG path data string
 * @returns {Array} Array of [x, y] coordinate pairs
 */
function parsePathData(pathData) {
	const coords = [];

	// Remove letters and split by spaces/commas, keeping only numeric values
	const parts = pathData
		.replace(/[A-Za-z]/g, " ")
		.split(/[\s,]+/)
		.filter((part) => part && !Number.isNaN(Number.parseFloat(part)))
		.map((part) => Number.parseFloat(part));

	// Group into [x, y] pairs
	for (let i = 0; i < parts.length - 1; i += 2) {
		if (!Number.isNaN(parts[i]) && !Number.isNaN(parts[i + 1])) {
			coords.push([parts[i], parts[i + 1]]);
		}
	}

	return coords;
}

/**
 * Calculate bounding box from coordinate points
 * @param {Array} coords - Array of [x, y] coordinate pairs
 * @returns {Object} Bounding box with min/max x/y values
 */
function calculateBoundingBox(coords) {
	if (coords.length === 0) return null;

	let minX = coords[0][0];
	let maxX = coords[0][0];
	let minY = coords[0][1];
	let maxY = coords[0][1];

	coords.forEach(([x, y]) => {
		if (x < minX) minX = x;
		if (x > maxX) maxX = x;
		if (y < minY) minY = y;
		if (y > maxY) maxY = y;
	});

	return { minX, maxX, minY, maxY };
}

/**
 * Calculate center point of a bounding box
 * @param {Object} bbox - Bounding box object
 * @returns {Object} Center coordinates {x, y}
 */
function calculateCenter(bbox) {
	return {
		x: (bbox.minX + bbox.maxX) / 2,
		y: (bbox.minY + bbox.maxY) / 2,
	};
}

/**
 * Extract state coordinates from embedded SVG paths
 * @returns {Object} State coordinates keyed by state abbreviation
 */
function extractAllStateCoordinates() {
	const stateCoords = {};

	// Get the overall map dimensions for normalization
	let globalMinX = Number.POSITIVE_INFINITY;
	let globalMaxX = Number.NEGATIVE_INFINITY;
	let globalMinY = Number.POSITIVE_INFINITY;
	let globalMaxY = Number.NEGATIVE_INFINITY;

	// First pass: find global bounds
	Object.keys(STATE_SVG_PATHS).forEach((stateCode) => {
		const pathData = STATE_SVG_PATHS[stateCode];
		const coords = parsePathData(pathData);
		const bbox = calculateBoundingBox(coords);

		if (bbox) {
			if (bbox.minX < globalMinX) globalMinX = bbox.minX;
			if (bbox.maxX > globalMaxX) globalMaxX = bbox.maxX;
			if (bbox.minY < globalMinY) globalMinY = bbox.minY;
			if (bbox.maxY > globalMaxY) globalMaxY = bbox.maxY;
		}
	});

	// Second pass: calculate normalized coordinates
	Object.keys(STATE_SVG_PATHS).forEach((stateCode) => {
		const pathData = STATE_SVG_PATHS[stateCode];
		const coords = parsePathData(pathData);
		const bbox = calculateBoundingBox(coords);

		if (bbox) {
			const center = calculateCenter(bbox);

			// Normalize to 0-1 range
			const normalizedX = (center.x - globalMinX) / (globalMaxX - globalMinX);
			const normalizedY = (center.y - globalMinY) / (globalMaxY - globalMinY);

			stateCoords[stateCode] = {
				x: normalizedX,
				y: normalizedY,
				// Store raw coordinates for debugging
				raw: center,
				bounds: bbox,
			};
		}
	});

	return stateCoords;
}

