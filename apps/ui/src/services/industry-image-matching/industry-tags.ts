// Helper function to generate comprehensive code arrays
function generateNAICSCodes(baseCodes: string[]): string[] {
  const codes = [...baseCodes];
  // Add variations for 2-6 char codes
  baseCodes.forEach(base => {
    if (base.length === 2) {
      // Generate 3-char variations
      for (let i = 0; i < 10; i++) {
        codes.push(`${base}${i}`);
      }
    } else if (base.length === 3) {
      // Generate 4-char variations
      for (let i = 1; i < 10; i++) {
        codes.push(`${base}${i}`);
      }
    }
  });
  return [...new Set(codes)]; // Remove duplicates
}

function generatePSCCodes(baseCodes: string[]): string[] {
  const codes = [...baseCodes];
  baseCodes.forEach(base => {
    if (base.length === 2) {
      // Generate 4-char variations
      for (let i = 1; i < 100; i++) {
        codes.push(`${base}${i.toString().padStart(2, '0')}`);
      }
    }
  });
  return [...new Set(codes)]; // Remove duplicates
}

